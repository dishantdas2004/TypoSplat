"""
TypographyDataset
==================
PyTorch Dataset for loading (RGB, depth, mesh, camera) tuples generated by the
Blender pipeline.

Design notes (from earlier decisions):
- Sample folders are NOT assumed contiguous. We build the sample list by
  scanning the output directory for folders that contain a complete set of
  required files. Gaps in numbering (deleted bad samples) are fine.
- view.png is RGBA (4-channel) as saved by the compositor. We slice to RGB.
- depth.exr is RAW, unclamped-at-load (clamp to 100.0 already happened at
  generation time in the Blender compositor). We do NOT apply the <50.0 mask
  here -- that happens in the training loop, not the dataset loader.
- mesh.ply is letters-only (backer/wall geometry excluded by design).
- metadata.json carries fx,fy,cx,cy, camera_quaternion, camera_translation,
  camera_to_world_matrix, camera_convention, string_content, extrusion_depth,
  mount_style, is_cursive, etc.
"""

import os
import json
import glob
import numpy as np
import torch
from torch.utils.data import Dataset
from PIL import Image

try:
    import OpenEXR
    import Imath
    _HAS_OPENEXR = True
except ImportError:
    _HAS_OPENEXR = False

try:
    from plyfile import PlyData
    _HAS_PLYFILE = True
except ImportError:
    _HAS_PLYFILE = False


REQUIRED_SUFFIXES = {
    "view": "*view*.png",
    "depth": "*depth*.exr",
    "mesh": "mesh.ply",
    "meta": "*.json",
}


def _find_one(sample_dir, pattern):
    matches = glob.glob(os.path.join(sample_dir, pattern))
    return matches[0] if matches else None


def load_exr_depth(path):
    """Load a single-channel float32 EXR depth map -> np.ndarray [H, W]."""
    if not _HAS_OPENEXR:
        raise ImportError("Install OpenEXR: pip install OpenEXR")
    exr_file = OpenEXR.InputFile(path)
    header = exr_file.header()
    dw = header['displayWindow']
    width = dw.max.x - dw.min.x + 1
    height = dw.max.y - dw.min.y + 1
    channels = list(header['channels'].keys())
    target = channels[0]
    pt = Imath.PixelType(Imath.PixelType.FLOAT)
    raw = exr_file.channel(target, pt)
    depth = np.frombuffer(raw, dtype=np.float32).reshape(height, width).copy()
    return depth


def load_mesh_vertices(path):
    """Load vertex positions from a PLY file -> np.ndarray [N, 3]."""
    if not _HAS_PLYFILE:
        raise ImportError("Install plyfile: pip install plyfile")
    ply = PlyData.read(path)
    v = ply['vertex']
    verts = np.stack([v['x'], v['y'], v['z']], axis=1).astype(np.float32)
    return verts


class TypographyDataset(Dataset):
    def __init__(self, dataset_dir, transform=None, max_samples=None):
        """
        dataset_dir: root folder containing numbered sample subfolders
                     (e.g. output/0/, output/1/, ...)
        transform:   optional callable applied to the image tensor
        max_samples: optionally cap the number of samples scanned (useful for
                     quick local smoke tests on a partial batch)
        """
        self.dataset_dir = dataset_dir
        self.transform = transform
        self.samples = self._scan_valid_samples(dataset_dir, max_samples)

        if len(self.samples) == 0:
            raise RuntimeError(
                f"No valid samples found in {dataset_dir}. "
                f"Check that folders contain view.png, depth.exr, mesh.ply, "
                f"and a metadata .json file."
            )

    def _scan_valid_samples(self, dataset_dir, max_samples):
        """
        Build the sample list by checking each subfolder for a COMPLETE set
        of required files. Non-numeric folders and incomplete samples are
        silently skipped (not assumed contiguous -- gaps from deleted bad
        samples are expected and fine).
        """
        valid = []
        if not os.path.isdir(dataset_dir):
            return valid

        candidate_dirs = sorted(
            [d for d in os.listdir(dataset_dir)
             if os.path.isdir(os.path.join(dataset_dir, d)) and d.isdigit()],
            key=lambda x: int(x)
        )

        for d in candidate_dirs:
            sample_dir = os.path.join(dataset_dir, d)
            view_path = _find_one(sample_dir, REQUIRED_SUFFIXES["view"])
            depth_path = _find_one(sample_dir, REQUIRED_SUFFIXES["depth"])
            mesh_path = os.path.join(sample_dir, REQUIRED_SUFFIXES["mesh"])
            meta_path = _find_one(sample_dir, REQUIRED_SUFFIXES["meta"])

            if not (view_path and depth_path and os.path.exists(mesh_path) and meta_path):
                continue  # incomplete sample, skip silently

            valid.append({
                "id": d,
                "dir": sample_dir,
                "view_path": view_path,
                "depth_path": depth_path,
                "mesh_path": mesh_path,
                "meta_path": meta_path,
            })

            if max_samples is not None and len(valid) >= max_samples:
                break

        return valid

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        entry = self.samples[idx]

        # --- RGB image: PNG is saved as RGBA, slice to RGB ---
        img = Image.open(entry["view_path"]).convert("RGBA")
        img_np = np.array(img).astype(np.float32) / 255.0  # [H, W, 4]
        img_rgb = img_np[:, :, :3]  # drop alpha -> [H, W, 3]
        image = torch.from_numpy(img_rgb).permute(2, 0, 1)  # [3, H, W]
        if self.transform is not None:
            image = self.transform(image)

        # --- Depth: raw EXR, background already clamped to 100.0 at gen time ---
        depth_np = load_exr_depth(entry["depth_path"])  # [H, W]
        depth = torch.from_numpy(depth_np).unsqueeze(0)  # [1, H, W]

        # --- Mesh: letters-only vertex set (variable N per sample) ---
        mesh_verts_np = load_mesh_vertices(entry["mesh_path"])  # [N, 3]
        mesh_vertices = torch.from_numpy(mesh_verts_np)

        # --- Metadata ---
        with open(entry["meta_path"], "r") as f:
            meta = json.load(f)

        camera_to_world = torch.tensor(meta["camera_to_world_matrix"], dtype=torch.float32)  # [4,4]
        camera_quaternion = torch.tensor(meta["camera_quaternion"], dtype=torch.float32)      # [4] (w,x,y,z)
        camera_translation = torch.tensor(meta["camera_translation"], dtype=torch.float32)    # [3]

        return {
            "sample_id": entry["id"],
            "image": image,                     # [3, H, W] float32, 0-1
            "depth": depth,                      # [1, H, W] float32, meters (bg=100.0)
            "mesh_vertices": mesh_vertices,       # [N, 3] float32, variable N
            "fx": torch.tensor(meta["fx"], dtype=torch.float32),
            "fy": torch.tensor(meta["fy"], dtype=torch.float32),
            "cx": torch.tensor(meta["cx"], dtype=torch.float32),
            "cy": torch.tensor(meta["cy"], dtype=torch.float32),
            "camera_quaternion": camera_quaternion,
            "camera_translation": camera_translation,
            "camera_to_world_matrix": camera_to_world,
            "camera_convention": meta.get("camera_convention", "blender_neg_z_forward"),
            "string_content": meta.get("string_content", ""),
            "extrusion_depth": torch.tensor(meta.get("extrusion_depth", 0.0), dtype=torch.float32),
            "mount_style": meta.get("mount_style", ""),
            "is_cursive": meta.get("is_cursive", False),
        }


def collate_variable_mesh(batch):
    """
    Custom collate_fn since mesh_vertices has variable N per sample and
    default_collate can't stack ragged tensors. Everything else stacks
    normally; mesh_vertices stays a list of [N_i, 3] tensors.
    """
    out = {}
    keys = batch[0].keys()
    for k in keys:
        if k == "mesh_vertices":
            out[k] = [b[k] for b in batch]  # list of variable-length tensors
        elif k in ("sample_id", "camera_convention", "string_content", "mount_style", "is_cursive"):
            out[k] = [b[k] for b in batch]  # non-tensor / string fields
        else:
            out[k] = torch.stack([b[k] for b in batch], dim=0)
    return out


if __name__ == "__main__":
    import sys
    from torch.utils.data import DataLoader

    dataset_dir = sys.argv[1] if len(sys.argv) > 1 else "./output"

    print(f"Scanning {dataset_dir} ...")
    ds = TypographyDataset(dataset_dir, max_samples=10)
    print(f"Found {len(ds)} valid samples")

    loader = DataLoader(ds, batch_size=2, shuffle=True, collate_fn=collate_variable_mesh)
    batch = next(iter(loader))

    print("\n=== Batch shape/dtype check ===")
    for k, v in batch.items():
        if isinstance(v, torch.Tensor):
            print(f"  {k}: shape={tuple(v.shape)} dtype={v.dtype}")
        elif isinstance(v, list) and len(v) > 0 and isinstance(v[0], torch.Tensor):
            print(f"  {k}: list of {len(v)} tensors, shapes={[tuple(t.shape) for t in v]}")
        else:
            print(f"  {k}: {v}")

    print("\n=== Sanity checks ===")
    img = batch["image"]
    depth = batch["depth"]
    print(f"  image range: [{img.min():.3f}, {img.max():.3f}] (expect ~[0,1])")
    print(f"  depth range: [{depth.min():.3f}, {depth.max():.3f}] (expect background ~100.0)")
    print(f"  depth has values <50 (foreground)?: {(depth < 50.0).any().item()}")